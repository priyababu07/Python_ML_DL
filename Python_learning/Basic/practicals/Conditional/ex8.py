val = input("Input any letters V,I,B,G,Y,O,R")

if val is "V" or "v":
    print("Violet")
elif val is "I" or 'i':
    print("Indigo")
elif val is "B" or 'b':
    print("Blue")
elif val is "G" or "g":
    print("Green")
elif val is "Y" or 'y':
    print("Yellow")
elif val is "O" or 'o':
    print("Orange")
elif val is "R" or 'r':
    print("Red")
else:
    print("Invalid")
